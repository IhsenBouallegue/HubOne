export default function ColorLuminance(hex: string, lum: number) {
  // validate hex string
  let cleanedHex = String(hex).replace(/[^0-9a-f]/gi, "");
  if (cleanedHex.length < 6) {
    cleanedHex =
      cleanedHex[0] +
      cleanedHex[0] +
      cleanedHex[1] +
      cleanedHex[1] +
      cleanedHex[2] +
      cleanedHex[2];
  }

  // convert to decimal and change luminosity
  let rgb = "#";
  for (let i = 0; i < 3; i += 1) {
    const c = parseInt(cleanedHex.substring(i * 2, i * 2 + 2), 16);
    const cStr = Math.round(Math.min(Math.max(0, c + c * lum), 255)).toString(
      16
    );
    rgb += `00${cStr}`.substring(cStr.length);
  }

  return rgb;
}

